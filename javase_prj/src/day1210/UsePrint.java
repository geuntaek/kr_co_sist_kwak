package day1210;

// 소스코드작성
class UsePrint{
	public static void main (String args[]){
		System.out.print("안녕");
		System.out.println("하세요");
		//정수출력 1,2,3,~,1000 등등
		System.out.println(12);
		//실수출력 3.14 같은거
		System.out.println(3.14);
		//문자상수출력 '한','a' ,'0' 숫자도 작은따옴표안에 들어있으면 문자라고 함
		System.out.println('a');
		//문자열출력 "아나바다"
		System.out.println("동방신기");
		//boolean
		System.out.println(true);
		//연산식 : 연산의 결과만 출력
		System.out.println(12+10);
		//문자열의 출력
		System.out.println("12+10="+(12+10));
	}//main
}//class
//저장 : UsePrint.java
//컴파일 : javac UsePrint.java
//실행 : java UsePrint